// C program to print all alphabets from a to z
#include<stdio.h>
int main()
{
    char ch;
    printf("Enter the Alphabets\n");


    for(ch='e';ch<='i';ch++)
    {

        printf("%c\n", ch);
    }
   for(ch='E';ch<='I'; ch++)
   {
       printf("%c\n", ch);
   }
    return 0;

}
